    public void readJTable() throws Exception {
        DefaultTableModel pacientes = (DefaultTableModel) jTablePacientesCadastrados.getModel();
        DAO dao = new DAO();
        pacientes.setNumRows(0);
        for (Paciente p : dao.mostraPaciente()) {

            pacientes.addRow(new Object[]{
                p.getCodigo(),
                p.getNome(),
                p.getCpf(),
                p.getNasctoo(),
                p.getIdade(),
                p.getTelefone(),
                p.getEmail(),
                p.getCep(),
                p.getRua(),
                p.getNumero(),
                p.getComplemento(),
                p.getBairro(),
                p.getCidade(),
                p.getEstado(),
                p.isAreaSaude(),
                p.getDose(),
                p.getVacinacao(),
                p.getVacinacao2(),
                p.getPrioridade(),});
        }
    }

