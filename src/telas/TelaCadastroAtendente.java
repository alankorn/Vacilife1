    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
        String nome = "%" + jTextFieldProcurar.getText() + "%";

        DAO dao = new DAO();
        ArrayList<Usuario> atendente = new ArrayList<Usuario>();
        try {
            atendente = dao.buscarAtdPorNome(nome);
        } catch (Exception ex) {
            Logger.getLogger(TelaCadastroAtendente.class.getName()).log(Level.SEVERE, null, ex);
        }

        DefaultTableModel atendentes = (DefaultTableModel) jTableAtendentes.getModel();
        atendentes.setNumRows(0);

        for (Usuario u : atendente) {
            atendentes.addRow(new Object[]{
                u.getId(),
                u.getNome(),
                u.getEmail(),});

        }
    }//GEN-LAST:event_jButtonPesquisarActionPerformed

