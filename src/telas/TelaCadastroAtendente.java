    private void jButtonOkzinhoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOkzinhoActionPerformed

        Usuario atd = new Usuario();
        int pegarSelecao = jTableAtendentes.getSelectedRow();
        Object iddd = jTableAtendentes.getValueAt(pegarSelecao, 0);
        String idd = iddd.toString();
        int id = Integer.parseInt(idd);

        if (jTextFieldEditarEmail.getText().equals("") || jTextFieldEditarNome1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha os campos obrigatórios");
        } else {
            atd.setEmail(jTextFieldEditarEmail.getText());
            atd.setNome(jTextFieldEditarNome1.getText());
            atd.setId(id);
            DAO dao = new DAO();

            try {
                dao.atualizarAtd(atd);

                JOptionPane.showMessageDialog(null, "Atendente atualizado com sucesso!");
                readJTableAtd();
                jTextFieldEditarEmail.setText("");
                jTextFieldEditarNome1.setText("");
                jTabbedPane1.setSelectedIndex(1);
            } catch (Exception ex) {
                Logger.getLogger(TelaCadastroAdmin.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Falha técnica, verifique se seu email já não está cadastrado. Caso não esteja, consulte o suporte!", "Falha no Cadastro", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButtonOkzinhoActionPerformed

    private void jButtonConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmarActionPerformed
        Usuario atd = new Usuario();
        int pegarSelecao = jTableAtendentes.getSelectedRow();
        Object iddd = jTableAtendentes.getValueAt(pegarSelecao, 0);
        String idd = iddd.toString();
        int id = Integer.parseInt(idd);

        String senha = new String(jPasswordFieldEditarSenha.getPassword());
        String CSenha = new String(jPasswordFieldEditarConfirmarSenha.getPassword());

        if (senha == null || senha.length() == 0 || CSenha == null || CSenha.length() == 0) {
            JOptionPane.showMessageDialog(null, "Preencha os campos obrigatórios!");
        } else if (senha.equals(CSenha)) {
            atd.setSenha(senha);
            atd.setId(id);
            DAO dao = new DAO();
            try {
                dao.atualizarSenhaAtd(atd);

                JOptionPane.showMessageDialog(null, "Senha atualizada com sucesso!");
                readJTableAtd();
                jPasswordFieldEditarSenha.setText("");
                jPasswordFieldEditarConfirmarSenha.setText("");
                jTabbedPane1.setSelectedIndex(1);
            } catch (Exception ex) {
                Logger.getLogger(TelaCadastroAdmin.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Os campos de senha devem ser os mesmos!");
        }
    }//GEN-LAST:event_jButtonConfirmarActionPerformed

