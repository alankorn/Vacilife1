    private void ButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSalvarActionPerformed
        String nomeAtd = jTextFieldNome.getText();
        String emailAtd = jTextFieldEmail.getText();
        String senha = new String(jPasswordFieldSenha.getPassword());
        String confirmaSenha = new String(jPasswordFieldConfirmarSenha.getPassword());
        if (nomeAtd == null || nomeAtd.length() == 0
                || emailAtd == null || emailAtd.length() == 0 || senha == null || senha.length() == 0
                || confirmaSenha == null || confirmaSenha.length() == 0) {
            JOptionPane.showMessageDialog(null, "Preencha os campos obrigatórios!");
        } else if (senha.equals(confirmaSenha)) {
            try {
                int escolha = JOptionPane.showConfirmDialog(null, "Confirmar cadastro de novo atendente?");
                if (escolha == JOptionPane.YES_OPTION) {
                    Usuario user = new Usuario(nomeAtd, emailAtd, senha, true);//true pq é um atendente;
                    DAO dao = new DAO();
                    dao.inserirAtendente(user);
                    JOptionPane.showMessageDialog(null, "Usuario atendente cadastrado com sucesso");
                    jTextFieldNome.setText("");
                    jTextFieldEmail.setText("");
                    jPasswordFieldSenha.setText("");
                    jPasswordFieldConfirmarSenha.setText("");
                    readJTableAtd();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Falha técnica, verifique se seu email já não está cadastrado. Caso não esteja, consulte o suporte!", "Falha no Cadastro", JOptionPane.WARNING_MESSAGE);
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Os campos de senha devem ser os mesmos!");
        }
    }//GEN-LAST:event_ButtonSalvarActionPerformed

    private void ButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonExcluirActionPerformed
        int pegarSelecao = jTableAtendentes.getSelectedRow();
        int escolha = JOptionPane.showConfirmDialog(null, "Remover atendente?");
        if (escolha == JOptionPane.YES_OPTION) {
            try {
                Object iddd = jTableAtendentes.getValueAt(pegarSelecao, 0);
                String idd = iddd.toString();
                int id = Integer.parseInt(idd);
                Usuario atd = new Usuario(id);
                DAO dao = new DAO();
                dao.removerAtd(atd);
                JOptionPane.showMessageDialog(null, "Atendete removido com sucesso!");
                readJTableAtd();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Falha técnica. Tente novamente mais tarde.");
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_ButtonExcluirActionPerformed

